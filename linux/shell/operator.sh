:<<!
算术运算符
  1. +	加法;
  2. -	减法;
  3. *	乘法，乘法比较特殊，需要 \*
  4. /	除法;
  5. %	取余
!


:<<!
比较运算符
  1. == 比较两个数字；
  2. =  比较字符串；
  3. != 字符串或者数字不相等；

  4. -eq	检测两个数是否相等，相等返回 true;
  5. -ne	检测两个数是否不相等，不相等返回 true;
  6. -gt	检测左边的数是否大于右边的，如果是，则返回 true;
  7. -lt	检测左边的数是否小于右边的，如果是，则返回 true;
  8. -ge	检测左边的数是否大于等于右边的，如果是，则返回 true;
  9. -le	检测左边的数是否小于等于右边的，如果是，则返回 true.
!

:<<!
逻辑运算符
  1. 非: !;
  2. 与: && -a (and)
  3. 或: || -o (or)
!